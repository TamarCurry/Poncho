namespace Poncho.Text
{
	/// <summary>
	/// Used to specify how the text in a TextField should be aligned.
	/// </summary>
	public enum TextAlignment
	{
		/// <summary>
		/// Text is aligned to the left side of the TextField.
		/// </summary>
		LEFT,

		/// <summary>
		/// Text is aligned to the right side of the TextField.
		/// </summary>
		RIGHT,

		/// <summary>
		/// Text is aligned to the center of the TextField.
		/// </summary>
		CENTER
	}
}
