
namespace Poncho.Geom
{
	public class ImageRectF
	{
		public float x { get; }
		public float y { get; }
		public float width { get; }
		public float height { get; }
		
		// --------------------------------------------------------------
		public ImageRectF(float x, float y, float width, float height)
		{
			this.x = x;
			this.y = y;
			this.width = width;
			this.height = height;
		}
	}
}
